# number type data type is int , float, complex
# invalid syntax number = 1f , number = 2.f etc

number = 60          # Data type is int
print(type(number))
print(number)

non_integer_number = 20.00  # Data type is float
print(type(non_integer_number))
print(non_integer_number)

complex_number = 3j    # Data type is complex 1 + 2j
print(type(complex_number))
print(complex_number)

